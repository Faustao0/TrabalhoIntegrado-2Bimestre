    private void jRadioButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButton2ActionPerformed
        ProdutoFrame produtoFrame = new ProdutoFrame();
        produtoFrame.setVisible(true);
        produtoFrame.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent windowEvent) {
                Produto produto = produtoFrame.getProdutoSelecionado();
                if (produto != null) {
                    try {
                        int quantidade = Integer.parseInt(JOptionPane.showInputDialog("Informe a quantidade:"));
                        BigDecimal valorUnitario = produto.getValor();
                        BigDecimal valorTotal = valorUnitario.multiply(BigDecimal.valueOf(quantidade));
                        ItemVenda itemVenda = new ItemVenda(null, quantidade, valorUnitario, valorTotal, null, produto);
                        itensVenda.add(itemVenda);
                        tabelaItemVenda.fireTableDataChanged();
                        atualizarPrecoTotal();
                    } catch (NumberFormatException e) {
                        JOptionPane.showMessageDialog(null, "Quantidade inválida");
                    }
                }
            }
        });
    }//GEN-LAST:event_jRadioButton2ActionPerformed

