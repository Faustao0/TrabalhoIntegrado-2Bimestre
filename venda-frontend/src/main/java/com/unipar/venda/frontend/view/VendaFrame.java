      private String fetchJsonFromUrl(URL url) throws Exception {
        HttpURLConnection connection = (HttpURLConnection) url.openConnection();
        BufferedReader in = new BufferedReader(new InputStreamReader(connection.getInputStream()));

        StringBuilder result = new StringBuilder();
        String inputLine;
        while ((inputLine = in.readLine()) != null) {
            result.append(inputLine);
        }

        in.close();
        connection.disconnect();
        return result.toString();
    }

    private <T> T unmarshalFromJson(String json, TypeReference<T> typeReference) throws Exception {
        ObjectMapper objectMapper = new ObjectMapper();
        return objectMapper.readValue(json, typeReference);
    }

    private void atualizarPrecoTotal() {
        BigDecimal total = calcularTotalVenda();
        jLabel5.setText("R$: " + total);
    }

    private BigDecimal calcularTotalVenda() {
        BigDecimal total = BigDecimal.ZERO;
        for (ItemVenda item : itensVenda) {
            total = total.add(item.getValorTotal());
        }
        return total;
    }
    
    private void limparCampos() {
    cliente = null;
    itensVenda.clear();
    tabelaItemVenda.fireTableDataChanged();
    jTextArea1.setText("");
    jTable1.setModel(new javax.swing.table.DefaultTableModel(
        new Object [][] {},
        new String[]{"ID", "Nome", "Telefone", "Email"}
    ));
    jLabel5.setText("R$: 0");
}
    
    private void finalizarVenda() {
        Venda venda = new Venda(null, jTextArea1.getText(), new Timestamp(System.currentTimeMillis()), calcularTotalVenda(), cliente, itensVenda);

        new SwingWorker<Void, Void>() {
            @Override
            protected Void doInBackground() throws Exception {
                try {
                    salvarVenda(venda);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(null, "Erro ao salvar venda: " + e.getMessage());
                    logVenda.registrarLog("Finalizar Venda", "Erro: " + e.getMessage());
                }
                return null;
            }

            @Override
            protected void done() {
                JOptionPane.showMessageDialog(null, "Venda salva com sucesso!");
                logVenda.registrarLog("Finalizar Venda", "Sucesso");
                limparCampos();
            }
        }.execute();
    }

    private void salvarVenda(Venda venda) throws Exception {
        URL url = new URL("http://localhost:8080/vendas");
        HttpURLConnection connection = (HttpURLConnection) url.openConnection();
        connection.setRequestMethod("POST");
        connection.setRequestProperty("Content-Type", "application/json; utf-8");
        connection.setDoOutput(true);

        ObjectMapper objectMapper = new ObjectMapper();
        String jsonInputString = objectMapper.writeValueAsString(venda);

        try (OutputStream os = connection.getOutputStream()) {
            byte[] input = jsonInputString.getBytes("utf-8");
            os.write(input, 0, input.length);
        }

        int code = connection.getResponseCode();
        if (code != 200 && code != 201) {
            throw new RuntimeException("Failed : HTTP error code : " + code);
        }

        BufferedReader in = new BufferedReader(new InputStreamReader(connection.getInputStream(), "utf-8"));
        StringBuilder response = new StringBuilder();
        String responseLine = null;
        while ((responseLine = in.readLine()) != null) {
            response.append(responseLine.trim());
        }

        in.close();
        connection.disconnect();
    }
  
